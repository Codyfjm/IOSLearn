#include <stdio.h>

/**
 *  求两个整型的数的和
 *
 *  @param num1 第一个整数
 *  @param num2 第二个整数
 *
 *  @return 传入的两个整数的河
 */
int getSum(int num1,int num2){
    return num1+num2;
}

/**
 *  求两个整型的数的差
 *
 *  @param num1 第一个整数
 *  @param num2 第二个整数
 *
 *  @return 传入的两个证书的差
 */
int getCha(int num1,int num2){
    return num1-num2;
}

/**
 *  求两个整型的数的积
 *
 *  @param num1 第一个整数
 *  @param num2 第二个整数
 *
 *  @return 传入的两个证书的积
 */
int getJi(int num1,int num2){
    return num1*num2;
}


/**
 *  求两个整型的数的商
 *
 *  @param num1 第一个整数
 *  @param num2 第二个整数
 *
 *  @return 传入的两个证书的商
 */
double getShang(int num1,int num2){
    return num1*1.0/num2;
}

/**
 *  求两个整型的数的模
 *
 *  @param num1 第一个整数
 *  @param num2 第二个整数
 *
 *  @return 传入的两个证书的模
 */
int getMo(int num1,int num2){
    return num1%num2;
}